// 1. Validação REAL de CPF/CNPJ
function validarCpfCnpj(valor) {
    const numeros = valor.replace(/\D/g, '');
    
    if (numeros.length === 11) {
        return validarCPF(numeros);
    } else if (numeros.length === 14) {
        return validarCNPJ(numeros);
    }
    return false;
}

function validarCPF(cpf) {
    if (cpf.length !== 11 || /^(\d)\1{10}$/.test(cpf)) return false;
    
    let soma = 0;
    for (let i = 0; i < 9; i++) {
        soma += parseInt(cpf.charAt(i)) * (10 - i);
    }
    let resto = (soma * 10) % 11;
    if (resto === 10 || resto === 11) resto = 0;
    if (resto !== parseInt(cpf.charAt(9))) return false;
    
    soma = 0;
    for (let i = 0; i < 10; i++) {
        soma += parseInt(cpf.charAt(i)) * (11 - i);
    }
    resto = (soma * 10) % 11;
    if (resto === 10 || resto === 11) resto = 0;
    return resto === parseInt(cpf.charAt(10));
}

function validarCNPJ(cnpj) {
    if (cnpj.length !== 14 || /^(\d)\1{13}$/.test(cnpj)) return false;
    
    const pesos1 = [5, 4, 3, 2, 9, 8, 7, 6, 5, 4, 3, 2];
    const pesos2 = [6, 5, 4, 3, 2, 9, 8, 7, 6, 5, 4, 3, 2];
    
    let soma = 0;
    for (let i = 0; i < 12; i++) {
        soma += parseInt(cnpj.charAt(i)) * pesos1[i];
    }
    let resto = soma % 11;
    let digito1 = resto < 2 ? 0 : 11 - resto;
    if (digito1 !== parseInt(cnpj.charAt(12))) return false;
    
    soma = 0;
    for (let i = 0; i < 13; i++) {
        soma += parseInt(cnpj.charAt(i)) * pesos2[i];
    }
    resto = soma % 11;
    let digito2 = resto < 2 ? 0 : 11 - resto;
    return digito2 === parseInt(cnpj.charAt(13));
}

// 2. Validação REAL de telefone brasileiro
function validarTelefoneBrasileiro(telefone) {
    const tel = telefone.replace(/\D/g, '');
    
    // Verifica se tem DDD (2 dígitos) + número (8 ou 9 dígitos)
    // Celular: 11 91234-5678 (11 dígitos) ou 11 1234-5678 (10 dígitos)
    // Fixo: 11 2345-6789 (10 dígitos)
    
    if (tel.length === 11) { // Celular com 9 dígitos
        return /^[1-9]{2}9[0-9]{8}$/.test(tel);
    } else if (tel.length === 10) { // Fixo ou celular antigo
        return /^[1-9]{2}[2-9][0-9]{7}$/.test(tel);
    }
    return false;
}

// 3. Funções de formatação
function formatarCpfCnpj(valor) {
    const numeros = valor.replace(/\D/g, '');
    if (numeros.length === 11) {
        return numeros.replace(/(\d{3})(\d{3})(\d{3})(\d{2})/, '$1.$2.$3-$4');
    } else if (numeros.length === 14) {
        return numeros.replace(/(\d{2})(\d{3})(\d{3})(\d{4})(\d{2})/, '$1.$2.$3/$4-$5');
    }
    return valor;
}

function formatarTelefone(telefone) {
    const tel = telefone.replace(/\D/g, '');
    if (tel.length === 11) {
        return tel.replace(/(\d{2})(\d{5})(\d{4})/, '($1) $2-$3');
    } else if (tel.length === 10) {
        return tel.replace(/(\d{2})(\d{4})(\d{4})/, '($1) $2-$3');
    }
    return telefone;
}

function formatarCEP(cep) {
    const numeros = cep.replace(/\D/g, '');
    if (numeros.length === 8) {
        return numeros.replace(/(\d{5})(\d{3})/, '$1-$2');
    }
    return cep;
}

// 4. FUNÇÃO PARA BLOQUEAR LETRAS EM CAMPOS NUMÉRICOS (adicione este evento nos seus campos)
function bloquearLetras(event) {
    // Permitir apenas: números, Backspace, Tab, setas, Delete
    const teclasPermitidas = [
        'Backspace', 'Tab', 'ArrowLeft', 'ArrowRight', 
        'Delete', 'Home', 'End'
    ];
    
    if (teclasPermitidas.includes(event.key)) return true;
    
    // Permitir apenas números
    if (!/^\d$/.test(event.key)) {
        event.preventDefault();
        return false;
    }
}

